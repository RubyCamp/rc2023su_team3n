require 'dxruby'

# グラフの表現
graph = {
  [0, 0] => [[0, 1], [1, 0]],
  [0, 1] => [[0, 0], [0, 2], [1, 1]],
  [0, 2] => [[0, 1], [0, 3], [1, 2]],
  [0, 3] => [[0, 2]],
  [1, 0] => [[0, 0], [1, 1], [2, 0]],
  [1, 1] => [[0, 1], [1, 0], [1, 2], [2, 1]],
  [1, 2] => [[0, 2], [1, 1], [1, 3], [2, 2]],
  [1, 3] => [[0, 2], [1, 2], [2, 3]],
  [2, 0] => [[1, 0], [2, 1]],
  [2, 1] => [[1, 1], [2, 0], [2, 2]],
  [2, 2] => [[1, 2], [2, 1], [2, 3]],
  [2, 3] => [[1, 3], [2, 2]],
}

# ウィンドウの設定
Window.width = 400
Window.height = 300

# メインループ
Window.loop do
  # マップ上の各セルを描画
  graph.each do |node, neighbors|
    x, y = node
    x_pos = y * 100
    y_pos = x * 100
    Window.draw_box_fill(x_pos, y_pos, x_pos + 100, y_pos + 100, [255, 255, 255])
    Window.draw_box(x_pos, y_pos, x_pos + 100, y_pos + 100, [0, 0, 0])
  end
end